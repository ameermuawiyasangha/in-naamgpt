def is_devanagari_name(name):
    """Check if a name contains only Devanagari characters (U+0900-U+097F) after NFD decomposition."""
    nfd = unicodedata.normalize("NFD", name)
    for ch in nfd:
        cp = ord(ch)
        if not (0x0900 <= cp <= 0x097F):
            return False
    return len(nfd) >= 2  # At least 2 NFD chars


def main():
    base_dir = Path(__file__).resolve().parents[1]
    data_path = base_dir / "data" / "in_name.txt"

    # Load existing names
    existing = set()
    if data_path.exists():
        with open(data_path, "r", encoding="utf-8") as f:
            for line in f:
                name = line.strip()
                if name:
                    existing.add(unicodedata.normalize("NFC", name))

    print(f"Existing names: {len(existing)}")

    # Collect all new names from curated lists
    all_sources = {
        "Boys Traditional": BOYS_TRADITIONAL,
        "Boys Modern": BOYS_MODERN,
        "Girls Traditional": GIRLS_TRADITIONAL,
        "Girls Modern": GIRLS_MODERN,
        "Vedic/Mythological": VEDIC_MYTHOLOGICAL,
        "Nature/Virtue": NATURE_VIRTUE,
        "Regional Diverse": REGIONAL_DIVERSE,
        "Popular 2020s": POPULAR_MODERN_2020S,
        "Short Names": SHORT_NAMES,
    }

    new_names = set()
    for category, names in all_sources.items():
        valid = 0
        for name in names:
            name = name.strip()
            if not name:
                continue
            nfc = unicodedata.normalize("NFC", name)
            if is_devanagari_name(nfc) and nfc not in existing:
                new_names.add(nfc)
                valid += 1
        print(f"  {category}: {valid} new valid names")

    print(f"\nNew names to add: {len(new_names)}")

    # Merge: existing + new, sort by NFC
    all_names = sorted(existing | new_names)

    # Write back
    with open(data_path, "w", encoding="utf-8") as f:
        for name in all_names:
            f.write(name + "\n")

    print(f"Total dataset: {len(all_names)} names")
    print(f"Written to: {data_path}")

    # Stats
    all_nfd_chars = set()
    for name in all_names:
        nfd = unicodedata.normalize("NFD", name)
        for ch in nfd:
            all_nfd_chars.add(ch)
    print(f"Unique NFD characters: {len(all_nfd_chars)}")


if __name__ == "__main__":
    main()
